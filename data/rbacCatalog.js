const PERMISSION_ALIAS_LOOKUP = Object.freeze(
  PERMISSION_DEFINITIONS.reduce((lookup, permission) => {
    lookup[permission.key] = permission.key;
    lookup[permission.constant.toLowerCase()] = permission.key;
    lookup[permission.constant] = permission.key;
    permission.legacyKeys.forEach((legacyKey) => {
      lookup[String(legacyKey).toLowerCase()] = permission.key;
    });
    return lookup;
  }, {}),
);

const normalizeRoleKey = (role) =>
  String(role || "")
    .trim()
    .toLowerCase()
    .replace(/[\s-]+/g, "_");

const normalizePermissionKey = (permission) => {
  const normalized = String(permission || "")
    .trim()
    .toLowerCase()
    .replace(/[\s-]+/g, "_");

  if (!normalized) {
    return "";
  }

  if (PERMISSION_ALIAS_LOOKUP[normalized]) {
    return PERMISSION_ALIAS_LOOKUP[normalized];
  }

  const dotted = normalized.replace(/_/g, ".");
  return PERMISSION_ALIAS_LOOKUP[dotted] || normalized;
};

const getPermissionDefinition = (permission) =>
  PERMISSION_DEFINITIONS.find(
    (definition) => definition.key === normalizePermissionKey(permission),
  ) || null;

const expandPermissionHierarchy = (permissions = []) => {
  const expandedPermissions = new Set();
  const stack = [...permissions];

  while (stack.length > 0) {
    const currentPermission = normalizePermissionKey(stack.pop());
    if (!currentPermission || expandedPermissions.has(currentPermission)) {
      continue;
    }

    expandedPermissions.add(currentPermission);

    const definition = getPermissionDefinition(currentPermission);
    const impliedPermissions = Array.isArray(definition?.impliedPermissions)
      ? definition.impliedPermissions
      : [];

    impliedPermissions.forEach((impliedPermission) => {
      const normalizedImplied = normalizePermissionKey(impliedPermission);
      if (normalizedImplied && !expandedPermissions.has(normalizedImplied)) {
        stack.push(normalizedImplied);
      }
    });
  }

  return [...expandedPermissions];
};

